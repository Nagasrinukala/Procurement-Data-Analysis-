-- I used STAR SCHEMA to avoid data duplication , improve query performance and easy to scale 

-- 1.Fact Table : fact_place
CREATE TABLE fact_procurement(
transaction_id INT PRIMARY KEY ,
supplier_id INT,
category_id INT,
amount DECIMAL(10,2)
);
ALTER TABLE fact_procurement
ADD COLUMN date_id INT AFTER category_id;


-- 2.Dimension Table : dim_supplier
CREATE TABLE dim_supplier(
supplier_id INT PRIMARY KEY,
supplier_name VARCHAR(100),
supplier_rating int
);

-- 3.Dimensio Table : dim_category
CREATE TABLE dim_category(
category_id INT PRIMARY KEY, 
category_name VARCHAR(100)
);

-- 4.Dimension Table : dim_date
CREATE TABLE dim_date(
date_id INT PRIMARY KEY,
full_date DATE,
Year INT,
month INT,
quarter INT
);

